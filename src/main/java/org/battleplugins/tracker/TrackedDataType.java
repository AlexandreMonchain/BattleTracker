package org.battleplugins.tracker;

/**
 * Represents the type of data being tracked.
 */
public enum TrackedDataType {
    /**
     * Represents player versus player data.
     */
    PVP,
    /**
     * Represents player versus enemy data.
     */
    PVE,
    /**
     * Represents player versus world data.
     */
    WORLD
}
